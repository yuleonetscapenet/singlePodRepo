import Foundation
import UIKit

extension UIColor {
  
  static var random: UIColor { return UIColor(Int.random(in: 0...0xFFFFFF)) }
  
  convenience init(
    red: Int,
    green: Int,
    blue: Int
    ) {
    self.init(
      red: CGFloat(red) / 255.0,
      green: CGFloat(green) / 255.0,
      blue: CGFloat(blue) / 255.0,
      alpha: 1.0
    )
  }
  
  convenience init(_ hex: Int) {
    self.init(
      red: (hex >> 16) & 0xFF,
      green: (hex >> 8) & 0xFF,
      blue: hex & 0xFF
    )
  }
}
